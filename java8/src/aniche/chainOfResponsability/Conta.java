package aniche.chainOfResponsability;

public class Conta {
	private String titular;
	private double saldo;

	public Conta(String titular, double salario) {
		this.titular = titular;
		this.saldo = salario;
	}

	public String getTitular() {
		return titular;
	}

	public double getSaldo() {
		return saldo;
	}
	
	
	

}
